### Пустота оркестра    
    
> Спой со мной и может быть у нас <br/>
> Получится обмануться <br/>
> Спой со мной и может быть вместе <br/>
> С песней и кожа вспыхнет огнем <br/>
> ОУ ОУ море отключится от кровати <br/>
> Спой лишь эти три слова <br/>
> И станет всё как прежде <br/>
> Как прежде <br/>

Попадая в симметрично организованное пространство с фиолетовым свечением от фитоламп и ярко зелёным светом из окон, оказываешься в месте для спиритического сеанса, где происходит контакт материального и не материального, живого и мёртвого. Где можно попробовать установить связь с призраком при помощи караоке.
   
В центре пространства стойка с микрофоном и экраном, на котором запущен караоке. Посетитель может занять место недостающего элемента и примерить на себя роль служителя мессы или медиума, вызывающего духов к жизни. Это пустое место само по себе призракологично. В своей обычной концепции призракология рассматривается как будущее которое не наступило: утопические модели мира, которые по каким-то причинам не прошли проверку реальности и остались в другом, сосуществующим с нами мире как призраки. В данной работе эта концепция обыгрывается со стороны всё таки возможности этого опыта, возможности занять несуществующее место, дать голос призракам, прорастить их, подключится к ним на их территории.
  
Субтитры к караоке -  переведённый через гугл переводчик текст популярной песни Bruno Mars – «The Other Side» с сильно замедленной минусовкой создают ситуацию в которой невозможно спеть вяло идущие слова на экране. Только в ситуации невозможности при помощи веры открываются условия для исполнения песни. Это очень похоже на ситуацию с восприятием искусства, ситуацию когда необходимо выйти из своих привычных ориентиров к другому (миру), месту где все координаты меняют градус. Зрителю предлагается спеть, предлагается поверить в искусство, но всегда ценной выхода из своего тела, ценой чистой веры - не рациональной и ничего не обещающей.
   
В пространстве галлюцинаторно соединяются взаимоисключающие образы: караоке-месса, фитолампы для роста растений, органические останки ваты, руинированной шерсти обжитой слизистыми проплешинами, внутренности этих руин – цифровые изображения: стоковые фотографии семьи, вымершие белые жирафы, любительские видео из дельфинария, причудливым коллажем заполняют и копошатся как опарыши внутри тела этих объектов-гробниц.
	
