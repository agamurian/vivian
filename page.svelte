### Void of the orchestra  

> Sing with me and maybe we have <br/>
> It will turn out to be deceived <br/>
> Sing with me and maybe together <br/>
> With song and skin will burst into flames <br/>
> Ooh ooh the sea will disconnect from the bed <br/>
> Sing just these three words <br/>
> And everything will become as before <br/>
> Like before <br/>

The exhibition takes place in the small, independent gallery "Kerka" (that translates from the Komi language as "Kerka" - "House"), which is structured more like a catacomb than a house. To get inside, the visitor needs to go through a network of courtyards and crouch under the roof to find himself inside a space with low ceilings, which is on the basement floor below ground level.

Once you enter the symmetrically arranged space with the purple illumination from the phyto-lamps and the bright green light from the windows, you find yourself in a place for a spiritual séance, where contact takes place between the material and the non-material, the living and the dead. Where you can try to connect with a ghost using karaoke.

In the center of the space is a stand with a microphone and a screen on which karaoke is running. The visitor can take the place of the missing element and try on the role of a mass servant or medium calling spirits into life. This empty space is ghostological in itself. In its usual conception, ghostology is seen as a future that has not come: utopian models of a world that, for whatever reason, have failed the test of reality existence and remain in another world coexisting with us as ghosts. In this work this concept is plotted against the possibility of this experience, the possibility of taking a non-existent place, of giving voice to ghosts, of sprouting them, of connecting to them on their territory.

The subtitles for karaoke, the text of the popular Bruno Mars song "Cross the line", translated through Google Translator, with heavily slowed down the backing track, create a situation where it is impossible to sing the sluggish words on the screen. Only in a situation of impossibility with the help of faith do the conditions for the song's performance open up. This is very similar to the situation with the perception of art, a situation where it is necessary to get out of your usual reference points to another (world), a place where all the coordinates change degrees. The viewer is offered to sing, offered to believe in art, but always at the cost of leaving one's body, at the cost of pure faith - not rational and not promising anything.

In the room, mutually exclusive/events/images combine in a hallucinatory way: karaoke masses, phyto-lamps for plant growth, organic remains of absorbent cotton, ruinous wool dwelling in slimy gaps, the insides of these ruins there are digital/events/images: stock photos of family, extinct white giraffes, amateur videos from a dolphinarium, fill and swarm like maggots inside the bodies of these tomb objects in a bizarre collage.  
      
